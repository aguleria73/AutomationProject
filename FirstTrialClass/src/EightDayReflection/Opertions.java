public class Opertions {
	
	private String name = "Ashish";
	
	public void getname() {
		System.out.println("Name:- " + name);
	}
	
	public void Add(int a, int b) {
		int sum = a + b;
		System.out.println("Add function:- " + sum);
	}
	
	public void Minus(int a, int b) {
		int diff = a - b;
		System.out.println("Minus function:- " + diff);
	}
	
	public void Multiply(int a, int b) {
		int multi = a * b;
		System.out.println("Multiply function:- " + multi);
	}
	
	public void Divison(int a, int b) {
		int div = a / b;
		System.out.println("Divison function:- " + div);
	}
	
	

}
